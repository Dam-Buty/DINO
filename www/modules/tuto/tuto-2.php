<div class="etape" id="etape-2-1">
    <h1>Primer campo</h1>
    <p>En DINO, un <b>mundo</b> debe tener a lo menos un nombre y un campo.</p>
    <p>Un campo de clasificacion es como una caja donde organizas tus documentos.</p>
    <p>Por ejemplo, puedes organizar tus documentos por <b>Cliente</b>, <b>Proyecto</b>, <b>Empleado</b>...</p>
